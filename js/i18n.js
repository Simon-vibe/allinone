class I18nManager {
    constructor() {
        if (typeof window !== 'undefined') {
            // æ ¹æ®å½“å‰ URL è·¯å¾„åˆ¤æ–­è¯­è¨€ (/zh/ vs /en/)
            this.currentLang = window.location.pathname.includes('/zh/') ? 'zh' : 'en';
            this.init();
        }
    }

    init() {
        this.updateUIState();
    }

    // æ ¸å¿ƒä¿®æ”¹ï¼šåªè´Ÿè´£è·³è½¬ URLï¼Œä¸å†ä¿®æ”¹ DOM æ–‡å­—
    setLanguage(lang) {
        if (this.currentLang === lang) return;

        // ä¿å­˜åå¥½ï¼ˆå¯é€‰ï¼Œä¸»è¦ç”¨äºä¸‹æ¬¡è®¿é—®æ ¹ç›®å½•æ—¶çš„è·³è½¬åˆ¤æ–­ï¼Œä½†ä¸å½±å“å½“å‰é¡µï¼‰
        localStorage.setItem('app_lang', lang);

        const currentPath = window.location.pathname;
        let newPath;

        // ç®€å•çš„è·¯å¾„æ›¿æ¢é€»è¾‘
        if (lang === 'zh') {
            // ä» /en/ åˆ‡æ¢åˆ° /zh/
            newPath = currentPath.replace('/en/', '/zh/');
            // å¦‚æœåŸæœ¬ä¸åœ¨ /en/ ä¸‹ï¼ˆæ¯”å¦‚æ ¹ç›®å½•è·³è½¬æ¥çš„ï¼‰ï¼Œå¼ºåˆ¶åŠ  /zh/
            if (newPath === currentPath) newPath = '/zh/' + currentPath.replace(/^\//, '');
        } else {
            // ä» /zh/ åˆ‡æ¢åˆ° /en/
            newPath = currentPath.replace('/zh/', '/en/');
        }

        // ç®€å•çš„å®¹é”™ï¼šé¿å… //
        newPath = newPath.replace('//', '/');

        window.location.href = newPath;
    }

    updateUIState() {
        // åªæ›´æ–°å¯¼èˆªæ æŒ‰é’®çš„æ¿€æ´»çŠ¶æ€
        const btnEn = document.getElementById('lang-en');
        const btnZh = document.getElementById('lang-zh');

        if (btnEn && btnZh) {
            // é‡ç½®æ ·å¼
            btnEn.classList.remove('font-bold', 'text-blue-600');
            btnZh.classList.remove('font-bold', 'text-blue-600');

            if (this.currentLang === 'en') {
                btnEn.classList.add('font-bold', 'text-blue-600');
            } else {
                btnZh.classList.add('font-bold', 'text-blue-600');
            }
        }
    }

    // ä¿ç•™ t() å‡½æ•°ä»¥é˜² JS åŠ¨æ€ç”Ÿæˆçš„ç»„ä»¶ï¼ˆå¦‚æœç´¢ç»“æœï¼‰éœ€è¦ç”¨åˆ°
    t(key) {
        if (!translations[this.currentLang]) return key;
        return translations[this.currentLang][key] || key;
    }
}

